    public void tampilData(){
        Object[][] objData = new Object[support.list.size()][support.column.length];
        int no = 0;
        for(User n : User.users){
            String[] data = {String.valueOf(no+1), n.getKodeUser(), n.getNama(), n.getUsername(), n.getLevel(), n.getPassword()};
            objData[no] = data;
            no++;
        }
        support.tampilData(objData);
    }
    
    
