    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        User e = new User(nama_lengkap.getText(), kode_user.getText(), username.getText(), password.getText(), level.getSelectedItem().toString());
        support.insertData(e);
        tampilData();
        clearForm();
        JOptionPane.showMessageDialog(null, "Data Berhasil Di Tambahkan!");
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void tableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUserMouseClicked
        kode_user.setText(support.getItemInTable(1));
        nama_lengkap.setText(support.getItemInTable(2));
        username.setText(support.getItemInTable(3));
        level.setSelectedItem(support.getItemInTable(4));
        password.setText(support.getItemInTable(5));
        btn_new.setEnabled(true);
        btn_simpan.setEnabled(false);
        btn_update.setEnabled(true);
        btn_delete.setEnabled(true);
        kode_user.setEnabled(false);
    }//GEN-LAST:event_tableUserMouseClicked

    private byte CariKode(){
        byte urutan = -1;
        for(byte i=0;  i < this.support.getList().size(); i++){
            if(this.support.getList().get(i).getKodeUser().equals(kode_user.getText())){
                    urutan = i;
                    break;
            }
        }
        return urutan;
    }
    
    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        byte urutan = this.CariKode();
        User u = new User(nama_lengkap.getText(), kode_user.getText(), username.getText(), password.getText(), level.getSelectedItem().toString());
        this.support.updateData(urutan, u);
        tampilData();
        clearForm();
        JOptionPane.showMessageDialog(null, "Data Berhasil Di Update!");
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
         int opsi = JOptionPane.showConfirmDialog(null, "Anda Yakin Ingin Menghapus Data Ini ?", 
                "Hapus Data", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        
        if (opsi == JOptionPane.YES_OPTION){
            this.support.deleteData(this.CariKode());
            tampilData();
            clearForm();
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Hapus!");
        }
        
    }//GEN-LAST:event_btn_deleteActionPerformed

