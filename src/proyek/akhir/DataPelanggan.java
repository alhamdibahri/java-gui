    public void tampilData(){
        Object[][] objData = new Object[support.list.size()][support.column.length];
        int no = 0;
        for(Pelanggan n : Pelanggan.pelanggan){
            String[] data = {String.valueOf(no+1), n.getKodePelanggan(), n.getNama(), n.getJk(), n.getTgl_lahir(), n.getAlamat(), n.getAgama(), n.getNohp(), n.getGolongan(), String.valueOf(n.getTarif()), n.getKeterangan()};
            objData[no] = data;
            no++;
        }
        support.tampilData(objData);
    }

