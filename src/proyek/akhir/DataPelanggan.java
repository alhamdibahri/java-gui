    private byte CariKode(){
        byte urutan = -1;
        for(byte i=0;  i < this.support.getList().size(); i++){
            if(this.support.getList().get(i).getKodePelanggan().equals(kode_pelanggan.getText())){
                    urutan = i;
                    break;
            }
        }
        return urutan;
    }
    
