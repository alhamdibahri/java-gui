    public void tampilData(){
        Object[][] objData = new Object[support.list.size()][support.column.length];
        int no = 0;
        for(Pembayaran n : Pembayaran.pembayaran){
            Object[] data = {no+1, n.getNoPembayaran(), n.getKodePelanggan(), n.getNama_petugas(), n.getTanggalBayar(), n.getDenda(), n.getTotal(), "Cetak Struk"};
            objData[no] = data;
            no++;
        }
        support.tampilData(objData);
        tablePembayaran.getColumnModel().getColumn(7).setCellRenderer(new ButtonRenderer());
        tablePembayaran.getColumnModel().getColumn(7).setCellEditor(new ButtonEditor(new JTextField()));
        
    }

