    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tmp = (String)kode_pelanggan.getSelectedItem();
        for(int i= 0; i<Pelanggan.pelanggan.size(); i++){
            if(Pelanggan.pelanggan.get(i).getKodePelanggan().equals(tmp)){
                nama_pelanggan.setText(Pelanggan.pelanggan.get(i).getNama());
                jenis_kelamin.setText(Pelanggan.pelanggan.get(i).getJk());
                alamat.setText(Pelanggan.pelanggan.get(i).getAlamat());
                no_handphone.setText(Pelanggan.pelanggan.get(i).getNohp());
                golongan.setText(Pelanggan.pelanggan.get(i).getGolongan());
                tarif.setText(String.valueOf(Pelanggan.pelanggan.get(i).getTarif()));
                keterangan.setText(Pelanggan.pelanggan.get(i).getKeterangan());
                tanggal_lahir.setText(Pelanggan.pelanggan.get(i).getTgl_lahir());
                agama.setText(Pelanggan.pelanggan.get(i).getAgama());
            }
        }
        konsumsi_daya.setText("Rp, " + String.valueOf(Double.parseDouble(tarif.getText()) * (Double.parseDouble(daya.getText()) / 1000 )));
        denda.setText("Rp, " + String.valueOf(Pembayaran.HitungDenda(String.valueOf(tanggal_pembayaran.getDayChooser().getDay()), golongan.getText())));
        total.setText("Rp, " + String.valueOf(Double.parseDouble(tarif.getText()) * (Double.parseDouble(daya.getText()) / 1000 ) + Pembayaran.HitungDenda(String.valueOf(tanggal_pembayaran.getDayChooser().getDay()), golongan.getText()) ));
        byte urutan = this.CariKode(no_pembayaran.getText());
        if(urutan == -1){
            btn_simpan6.setEnabled(true);
            btn_update6.setEnabled(false);
            btn_delete6.setEnabled(false);
        }else{
            btn_simpan6.setEnabled(false);
            btn_update6.setEnabled(true);
            btn_delete6.setEnabled(true);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

