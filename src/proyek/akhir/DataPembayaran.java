    private byte CariKode(String data){
        byte urutan = -1;
        for(byte i=0;  i < this.support.getList().size(); i++){
            if(this.support.getList().get(i).getNoPembayaran().equals(data)){
                    urutan = i;
                    break;
            }
        }
        return urutan;
    }
    
