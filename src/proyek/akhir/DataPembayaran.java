    private void btn_delete6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delete6ActionPerformed
        int opsi = JOptionPane.showConfirmDialog(null, "Anda Yakin Ingin Menghapus Data Ini ?",
            "Hapus Data", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (opsi == JOptionPane.YES_OPTION){
            this.support.deleteData(this.CariKode(no_pembayaran.getText()));
            clearForm();
            tampilData();
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Hapus!");
        }
    }//GEN-LAST:event_btn_delete6ActionPerformed

