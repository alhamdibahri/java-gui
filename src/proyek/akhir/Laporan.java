    private String bulan(String data){
        if(data.equals("Januari")){
            return "1";
        }else if(data.equals("Februari")){
            return "2";
        }else if(data.equals("Maret")){
            return "3";
        }else if(data.equals("April")){
            return "4";
        }else if(data.equals("Mei")){
            return "5";
        }else if(data.equals("Juni")){
            return "6";
        }else if(data.equals("Juli")){
            return "7";
        }else if(data.equals("Agistus")){
            return "8";
        }else if(data.equals("September")){
            return "9";
        }else if(data.equals("Oktober")){
            return "10";
        }else if(data.equals("November")){
            return "11";
        }else if(data.equals("Desember")){
            return "12";
        }else{
            return null;
        }
    }
    
