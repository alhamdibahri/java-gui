    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Object[][] objData = new Object[support.list.size()][support.column.length];
        int no = 0;
        for(Pembayaran n : Pembayaran.pembayaran){
            if(n.getTanggalBayar().substring(3,5).equalsIgnoreCase(this.bulan(bulan.getSelectedItem().toString())) &&
			n.getTanggalBayar().substring(6,10).equalsIgnoreCase(tahun.getSelectedItem().toString())){
                Object[] data = {no+1, n.getNoPembayaran(), n.getKodePelanggan(), n.getNama_petugas(), n.getTanggalBayar(), n.getDenda(), n.getTotal()};
                objData[no] = data;
                no++;
                
            }
        }
        support.tampilData(objData);
    }//GEN-LAST:event_jButton2ActionPerformed

