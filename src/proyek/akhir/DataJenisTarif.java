    private void btn_simpan2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpan2ActionPerformed
        JenisTarif e = new JenisTarif(
            golongan.getText(),
            Double.parseDouble(tarif.getText()),
            keterangan.getText());
        support.insertData(e);
        clearForm();
        tampilData();
        JOptionPane.showMessageDialog(null, "Data Berhasil Di Tambahkan!");
    }//GEN-LAST:event_btn_simpan2ActionPerformed

    private byte CariKode(){
        byte urutan = -1;
        for(byte i=0;  i < this.support.getList().size(); i++){
            if(this.support.getList().get(i).getGolongan().equals(golongan.getText())){
                    urutan = i;
                    break;
            }
        }
        return urutan;
    }
    
    private void btn_update2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_update2ActionPerformed
        byte urutan = this.CariKode();
        JenisTarif e = new JenisTarif(
            golongan.getText(),
            Double.parseDouble(tarif.getText()),
            keterangan.getText());
        this.support.updateData(urutan, e);
        clearForm();
        tampilData();
        JOptionPane.showMessageDialog(null, "Data Berhasil Di Update!");
    }//GEN-LAST:event_btn_update2ActionPerformed

    private void btn_delete2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delete2ActionPerformed
        int opsi = JOptionPane.showConfirmDialog(null, "Anda Yakin Ingin Menghapus Data Ini ?",
            "Hapus Data", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (opsi == JOptionPane.YES_OPTION){
            this.support.deleteData(this.CariKode());
            clearForm();
            tampilData();
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Hapus!");
        }
    }//GEN-LAST:event_btn_delete2ActionPerformed

    private void tableJenisTarifMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableJenisTarifMouseClicked
        golongan.setText(support.getItemInTable(1));
        tarif.setText(support.getItemInTable(2));
        keterangan.setText(support.getItemInTable(3));
        btn_new2.setEnabled(true);
        btn_simpan2.setEnabled(false);
        btn_update2.setEnabled(true);
        btn_delete2.setEnabled(true);
        golongan.setEnabled(false);
    }//GEN-LAST:event_tableJenisTarifMouseClicked

    private void clearForm(){
        golongan.setText("");
        keterangan.setText("");
        tarif.setText("");
        btn_new2.setEnabled(true);
        btn_simpan2.setEnabled(false);
        btn_update2.setEnabled(false);
        btn_delete2.setEnabled(false);
        golongan.setEnabled(true);
    }
    
