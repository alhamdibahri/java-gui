    public void tampilData(){
        Object[][] objData = new Object[support.list.size()][support.column.length];
        int no = 0;
        for(JenisTarif n : JenisTarif.jenis_tarif){
            String[] data = {String.valueOf(no+1), n.getGolongan(), String.valueOf(n.getTarif()), n.getKeterangan()};
            objData[no] = data;
            no++;
        }
        support.tampilData(objData);
    }

